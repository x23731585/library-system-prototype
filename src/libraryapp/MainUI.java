    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            int row = jTable1.getSelectedRow();

    if (row == -1) {
        jLabel2.setText("Select a book first.");
        return;
    }

    int bookId = (int) jTable1.getValueAt(row, 0);

    Loan loan = library.borrowBook(bookId);

    if (loan != null) {
        jLabel2.setText("Book borrowed successfully.");
        showBooks(library.getAllBooks());
    } else {
        jLabel2.setText("Book not available.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

