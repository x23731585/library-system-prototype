    private void showBooks(java.util.List<Book> books) {
    javax.swing.table.DefaultTableModel model =
        (javax.swing.table.DefaultTableModel) jTable1.getModel();

    model.setRowCount(0); // clear table

    for (Book b : books) {
        model.addRow(new Object[]{
            b.getId(),
            b.getTitle(),
            b.getAuthor(),
            b.isAvailable()
        });
    }
}

