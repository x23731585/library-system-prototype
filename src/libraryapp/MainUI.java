    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String title = jTextField1.getText().trim();

    if (title.isEmpty()) {
        jLabel2.setText("Enter a title.");
        return;
    }

    java.util.List<Book> results = library.searchBooks(title);
    showBooks(results);

    if (results.isEmpty()) {
        jLabel2.setText("No books found.");
    } else {
        jLabel2.setText(results.size() + " result(s) found.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

